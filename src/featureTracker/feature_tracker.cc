void FeatureTracker::drawTrack(const cv::Mat &imLeft,vector<int> &curLeftIds,vector<cv::Point2f> &curLeftPts, map<int, cv::Point2f> &prevLeftPtsMap)
{
    int cols = imLeft.cols;
    imTrack = imLeft.clone();
    cv::cvtColor(imTrack, imTrack, COLOR_GRAY2RGB);
    for (size_t j = 0; j < curLeftPts.size(); j++)
    {
        double len = std::min(1.0, 1.0 * track_cnt[j] / 20);
        cv::circle(imTrack, curLeftPts[j], 2, cv::Scalar(255 * (1 - len), 0, 255 * len), 2);
    }
    map<int, cv::Point2f>::iterator mapIt;
    for (size_t i = 0; i < curLeftIds.size(); i++)
    {
        int id = curLeftIds[i];
        mapIt = prevLeftPtsMap.find(id);
        if(mapIt != prevLeftPtsMap.end())
        {
            cv::arrowedLine(imTrack, curLeftPts[i], mapIt->second, cv::Scalar(0, 255, 0), 1, 8, 0, 0.2);
        }
    }
    viewer_ptr->UpdateImage(imTrack);
    // cv::imshow("imTrack", imTrack);
    // cv::waitKey(5);
}
